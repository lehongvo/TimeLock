const tokenContractAddress = "0x879e40448DD9BD788436a101c35C7Eb480F0fFFC";

async function getPermitSignature(spender, value, deadline) {
  try {
    const provider = new ethers.providers.JsonRpcProvider(munbai);
    const signer = new ethers.Wallet(privateKey, provider);
    console.log('%c04_erc20_permit.js line:500 signer.address', 'color: #007acc;', signer.address);

    const tokenERC721PermitContract = new ethers.Contract(
      tokenContractAddress,
      tokenERC721PermitABI,
      provider
    );

    const eip712Domain = await tokenERC721PermitContract.eip712Domain();
    console.log("=============eip712Domain=============", eip712Domain);

    const currentNonce = Number(
      await tokenERC721PermitContract.nonces(signer.address)
    );
    console.log("=============CurrentNonce=============", currentNonce);

    const verifyingContract = eip712Domain.verifyingContract;

    const [nonce, name, version, chainId] = await Promise.all([
      currentNonce,
      eip712Domain.name,
      eip712Domain.version,
      eip712Domain.chainId,
    ]);
    console.log(
      "%c04_erc20_permit.js line:23 nonce, name, version, chainId",
      "color: #007acc;",
      nonce,
      name,
      version,
      chainId
    );

    const signTypedData = await signer._signTypedData(
      {
        name: name,
        version: version,
        chainId: chainId,
        verifyingContract: verifyingContract,
      },
      {
        Permit: [
          { name: "owner", type: "address" },
          { name: "spender", type: "address" },
          { name: "value", type: "uint256" },
          { name: "nonce", type: "uint256" },
          { name: "deadline", type: "uint256" },
        ],
      },
      {
        owner: signer.address,
        spender: spender,
        value: value,
        nonce: nonce,
        deadline: deadline,
      }
    );

    const dataSignature = ethers.utils.splitSignature(signTypedData);
    console.log(
      "%c04_erc20_permit.js line:62 dataSignature",
      "color: #007acc;",
      dataSignature
    );
  } catch (error) {
    console.log(error);
  }
}

